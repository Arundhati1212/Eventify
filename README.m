Eventify
As a frontend developer dedicated to crafting immersive web experiences, I'm excited to unveil Eventify, my latest project. With Eventify, I've leveraged the power of React, TanStack Query, and cutting-edge React functionalities to deliver a dynamic and efficient event management platform.

Project Overview
Eventify: Eventify is a dynamic event management platform that allows users to effortlessly search, view, and manage events. Built with React and TanStack Query, this application ensures optimal performance and a seamless user experience by streamlining data fetching and caching.

Key Features and Technologies Utilized
TanStack Query: Leveraged TanStack Query to streamline data fetching and caching, ensuring optimal performance and scalability while reducing boilerplate code and complexity.
Dynamic Event Search: Implemented a dynamic search functionality allowing users to search for events based on keywords, enhancing user experience and accessibility.
React Routing: Utilized React Router for client-side routing, enabling smooth navigation between different pages and ensuring a seamless browsing experience for users.


How to Install and Run the Project
To run MealMate locally, follow these steps:
1.	Clone the repository: git clone https://github.com/Arundhati1212/Eventify.git
2.	Navigate to the project directory: cd mealmate 
3.	Install dependencies: npm install 
4.	Start the development server: 
    /backend : npm start
    / : npm run dev 
The application should now be running on http://localhost:5173.

